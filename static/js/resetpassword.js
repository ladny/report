//------------密码显示隐藏切换----------------开始
$(".eye-close-black").on("click",function(){
	if ($("#pwd").attr("type") == "password") {
        $("#pwd").attr("type", "text");
        $(".eye-close-black").css("background","url(../../static/imgs/login/eye-open-black.png) no-repeat right center");
    	$(".eye-close-black").css("background-size","0.426667rem");
	}
    else {
        $("#pwd").attr("type", "password")
        $(".eye-close-black").css("background","url(../../static/imgs/login/eye-close-black.png) no-repeat right center");
    	$(".eye-close-black").css("background-size","0.426667rem");
    }
})
//------------密码显示隐藏切换----------------结束